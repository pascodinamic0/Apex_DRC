export type Lang = "fr" | "en";
export type Dict = typeof dict.fr;

const I18nCtx = createContext<{ lang: Lang; t: Dict; setLang: (l: Lang) => void }>({
  lang: "fr",
  t: dict.fr,
  setLang: () => {},
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("fr");

  useEffect(() => {
    const saved = typeof window !== "undefined" ? (localStorage.getItem("lang") as Lang | null) : null;
    if (saved === "fr" || saved === "en") setLangState(saved);
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem("lang", l);
  };

  return <I18nCtx.Provider value={{ lang, t: dict[lang], setLang }}>{children}</I18nCtx.Provider>;
}

export const useT = () => useContext(I18nCtx);
